package session_5thNov2022_10thClass;

public class WrapperClassesPart1 {

	public static void main(String[] args) {
		
		int a = 20; //a is a variable of (int) primitive data type which store a value 20 
        
		Integer b = 30;// b is an object of integer type which holds a value 30
		
		System.out.println("Primitive value: " + a);//prints 20
		
		System.out.println("Wrapper Class Reference variable: " + b); // prints 30
		
	}
}
