package session_14thNov2022_14thClassReview;

public class Concatenation_Review {

	public static void main(String[] args) {
		// Concatenation is Joining not Adding
		
		String S1 = "Hello";
		String S2 = "Java";
		int i = 10;
		int j = 20;
		System.out.println(S1 + S2 + (i + j)); //Add (parenthesis) to the variables to Add
		System.out.println(S1 + S2 + i + j);

	}

}
